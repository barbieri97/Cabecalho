    def monta_cabecalho(self, frase):    
        """ Essa função recebe uma string contendo apenas letras e printa um 
        cabecalho com a frase passada """
        frase_corrigida = frase.replace(' ', '')
        for j in range(14): # todas as letras aqui criadas tem 13 linhas
            linha = ''
            for i in frase_corrigida:
                lista = self.dicionario_letras[i].split('\n')
                linha += f'{lista[j]}'
            print(linha)
       
                
    def imagem_ascii(self, imagem):

        """ Essa função recebe uma imagem e printa,
        o correspondente da imagem com caracteres ascii """
        
        caracteres = ["@", "#", "S", "%", "?", "*", "+", ";", ":", ",", "."]
        img = cv2.imread(imagem)
        imagem = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        altura, largura = imagem.shape
        altura = altura//8
        largura = largura//2
        image_resized = cv2.resize(imagem, (largura, altura))
        for line in image_resized:
            linha = ''.join([caracteres[pixel//25] for pixel in line])
            linha += '\n'
            print(linha)
